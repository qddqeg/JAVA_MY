package JAVA_1218;

public class Node {

	private int x;
	private int y;

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public void SetX(int x) {
		this.x = x;
	}

	public void SetY(int y) {
		this.y = y;
	}

	public Node(int x, int y) {
		this.x = x;
		this.y = y;
	}
}
